package basic.bean;

/**
 * JavaBean的定义
 *
 * 1.类是公共的
 * 2.有无参构造器
 * 3.有Get/Set方法
 */
public class Student {

    int id;

    String name;

    int age;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    @Override
    public String toString() {
        return "Student{" +
                "id=" + id +
                ", name='" + name + '\'' +
                ", age=" + age +
                '}';
    }
}
